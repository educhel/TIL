# 기본 자료형
# 1. 숫자형 : 정수형 (int) -> 소수점이 없을 때! (양/음/0)
print(type(1))

# 숫자형 : 실수형 (float) -> 소수점이 있을 때!
print(type(1.0))

# 2. 문자형 (str) : 문자의 형태를 가진 모든 글자들
print(type("문자열입니다."))
print('문자열 입니다2')
print("abcd")
print('!@$@#%')

print("0")
print(0)

print(type("0"))
print(type(" "))

# print('문자열 만들 수 있을까요?") -> 에러 발생
print('"쌍따옴표를 쓰고 싶다면?"')
print("'작은따옴표를 쓰고 싶다면?'")
print('\'작은따옴표를 쓰고 싶다면?\'')

# 3. 불린형
print(True) # 참
print(False) # 거짓
print(type(True))